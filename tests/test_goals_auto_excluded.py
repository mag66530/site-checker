"""Тест: автоцели Метрики исключены из «Проверки целей».

Автоцели (t=='auto': «Автосбор…», клики tel/mailto, отправка формы) и составные
«лид»-цели («Основная цель на лиды»/«Весь сайт») Яндекс считает сам на сервере -
извне факт срабатывания не виден. По просьбе их вообще не проверяем и не выводим.
"""
import os
import sys

sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

import goals_tester as G


def test_auto_and_lead_goals_excluded(monkeypatch):
    # Внешние зависимости (файлы проекта) - в пустышки.
    monkeypatch.setattr(G, '_результаты_форм', lambda pid: {})
    monkeypatch.setattr(G, '_формные_цели', lambda pid: set())
    monkeypatch.setattr(G, '_формные_url', lambda pid: set())

    каталог = {
        'проект': 'Тест', 'счётчик': '123', 'домен': 'x.ru',
        'цели': [
            {'тип': 'auto', 'номер': 1, 'название': 'Автосбор: покупка',
             'условие': 'форма', 'идентификаторы': []},
            {'тип': 'auto', 'номер': 2, 'название': 'Автоцель: заполнил контакты',
             'условие': 'форма', 'идентификаторы': []},
            {'тип': 'js', 'номер': 3, 'название': 'Основная цель на лиды',
             'условие': '', 'идентификаторы': ['lid_goal']},          # лид = тоже авто
            {'тип': 'js', 'номер': 4, 'название': 'Заказ звонка',
             'условие': 'reachGoal callback', 'идентификаторы': ['callback']},
        ],
    }
    прогон = {'fired': set(), 'страницы': [{'код': 200, 'url': 'https://x.ru/'}],
              'привязки': set(), 'код': '', 'идентификаторы': set()}

    res = G._классифицировать('__test_auto__', каталог, прогон)
    names = [s['название'] for s in res['строки']]

    assert 'Автосбор: покупка' not in names
    assert 'Автоцель: заполнил контакты' not in names
    assert 'Основная цель на лиды' not in names        # лид-автоцель тоже убрана
    assert 'Заказ звонка' in names                     # обычная цель осталась
    # «Всего» считается по фактически показанным строкам (без автоцелей).
    assert res['всего'] == len(res['строки']) == 1
    # В отчёте не осталось статусов «Автоцель (Метрика сама)».
    assert all(s['статус'] != 'Автоцель (Метрика сама)' for s in res['строки'])
    print('✓ автоцели и лид-цели исключены из «Проверки целей»')


if __name__ == '__main__':
    import pytest
    raise SystemExit(pytest.main([__file__, '-v', '-s']))
