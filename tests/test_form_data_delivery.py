"""Тест «Данные формы дошли до сервера (видно в DevTools → Network)»:
сверка снимка заполненных полей с реально пойманным телом POST-запроса.
Проверяются чистые функции (без браузера)."""
import sys
from pathlib import Path

import pytest

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT / "forms_tester"))

# test_all тянет bs4/playwright на уровне модуля - если их нет, тест пропускаем.
t = pytest.importorskip("test_all")


# ── данные_формы_вердикт ───────────────────────────────────────────────────

def test_все_поля_найдены_да():
    # «Тело» тут - уже декодированный текст (то, что отдаёт
    # _тело_запроса_для_поиска - см. тесты на неё ниже); данные_формы_вердикт
    # сам декодированием не занимается.
    статус, деталь = t.данные_формы_вердикт(
        True, "name=Тест Тестов&phone=79161234567&email=test@mail.ru",
        {"name": "Тест Тестов", "phone": "79161234567", "email": "test@mail.ru"})
    assert статус == "Да"
    assert деталь


def test_поле_отсутствует_не_долетело():
    статус, деталь = t.данные_формы_вердикт(
        True, "name=Тест+Тестов", {"name": "Тест Тестов", "phone": "79161234567"})
    assert статус == "Не долетело"
    assert "phone" in деталь


def test_нет_пойманного_запроса_проверить_а_не_не_долетело():
    # Регрессия против ложного срабатывания: не поймали ни одного POST
    # (обычная навигация без AJAX, сбой перехвата и т.п.) - это «Проверить»,
    # НЕ «Не долетело» (мы не знаем, что реально ушло - не обвиняем сайт).
    статус, деталь = t.данные_формы_вердикт(False, "", {"name": "Тест Тестов"})
    assert статус == "Проверить"
    assert деталь


def test_нет_заполненных_полей_проверить():
    статус, деталь = t.данные_формы_вердикт(True, "body", {})
    assert статус == "Проверить"
    статус2, _ = t.данные_формы_вердикт(True, "body", {"пусто": "   "})
    assert статус2 == "Проверить"


# ── _значение_долетело: устойчивость к легитимному переформатированию ────

def test_телефон_найден_по_цифровому_хвосту_после_маски():
    # В поле вписали чистые цифры (normalize_phone_for_submit), а сайт
    # переформатировал в маску перед отправкой - это не потеря данных.
    assert t._значение_долетело("79161234567", "phone=%2B7+(916)+123-45-67", "79161234567")


def test_телефон_не_найден_если_цифр_нет_вовсе():
    assert not t._значение_долетело("79161234567", "phone=", "")


def test_email_найден_без_учёта_регистра():
    assert t._значение_долетело("Test@Mail.ru", "email=test@mail.ru", "")


def test_длинный_комментарий_найден_по_обрезанному_префиксу():
    длинный = "Тестирование формы для проверки работоспособности сайта"
    # Сайт обрезал по своему maxlength, но начало сохранил - не потеря данных.
    тело = длинный[:20]
    assert t._значение_долетело(длинный, тело, "")


def test_комментарий_с_совсем_коротким_обрубком_не_найден():
    длинный = "Тестирование формы для проверки работоспособности сайта"
    # Обрубок короче порога префикса (20 симв.) - совпадения быть не должно,
    # иначе фолбэк станет всепрощающим и перестанет ловить реальные потери.
    assert not t._значение_долетело(длинный, длинный[:5], "")


def test_точное_совпадение_и_короткие_значения():
    assert t._значение_долетело("привет", "тут есть привет и всё", "")
    # Совсем короткие значения (шум) считаются найденными автоматически.
    assert t._значение_долетело("1", "совершенно другой текст", "")


# ── _плоские_значения: разбор вложенного JSON-тела ────────────────────────

def test_плоские_значения_вложенный_json():
    out = t._плоские_значения({"data": {"phone": "79161234567", "extra": ["a", "b"]}})
    assert set(out) == {"79161234567", "a", "b"}


def test_плоские_значения_скаляр_и_none():
    assert t._плоские_значения("просто строка") == ["просто строка"]
    assert t._плоские_значения(None) == []


# ── _тело_запроса_для_поиска: urlencoded/json/multipart ───────────────────

def test_тело_urlencoded_разбирается_через_parse_qsl():
    тело = t._тело_запроса_для_поиска(
        "application/x-www-form-urlencoded", "name=%D0%98%D0%B2%D0%B0%D0%BD")
    assert "Иван" in тело


def test_тело_json_разбирается_и_плоские_значения_попадают_в_текст():
    тело = t._тело_запроса_для_поиска(
        "application/json", '{"fields": {"phone": "79161234567"}}')
    assert "79161234567" in тело


def test_тело_multipart_остаётся_сырым_текстом():
    raw = '------X\r\nContent-Disposition: form-data; name="phone"\r\n\r\n79161234567\r\n------X--'
    тело = t._тело_запроса_для_поиска("multipart/form-data; boundary=----X", raw)
    assert "79161234567" in тело


def test_тело_разбор_падает_откатывается_на_сырой_текст():
    # Битый JSON (сервер/сайт мог отправить не совсем валидный JSON) - не
    # должно ронять всю проверку, только откатываемся на сырой текст.
    тело = t._тело_запроса_для_поиска("application/json", "{не json, phone: 79161234567}")
    assert "79161234567" in тело


# ── _снять_поля_формы: заглушка вместо браузера ────────────────────────────

class _FakeForm:
    def __init__(self, result=None, raise_error=False):
        self._result, self._raise = result, raise_error

    def evaluate(self, js):
        if self._raise:
            raise RuntimeError("страница уже закрыта")
        return self._result


def test_снять_поля_формы_возвращает_результат_evaluate():
    form = _FakeForm({"name": "Тест Тестов", "phone": "79161234567"})
    r = t._снять_поля_формы(form)
    assert r["поля"] == {"name": "Тест Тестов", "phone": "79161234567"}
    assert r["ошибка"] is False


def test_снять_поля_формы_гасит_исключение():
    form = _FakeForm(raise_error=True)
    r = t._снять_поля_формы(form)
    assert r["поля"] == {}
    assert r["ошибка"] is True


