"""Тест: цели-«заглушки» исключены из отчёта «Проверка целей».

Из отчёта убираем строки, которые нечего проверять/показывать:
«Не найдена/найдено на сайте», «Нет в коде сайта», «Форма не в этом прогоне»
и «Проверяется формами» (последняя - подсказка «запустите формы»: формы теперь
гоняются сами внутри проверки целей, ручных «подтягиваний» не предлагаем).
Такие строки не выводим и не учитываем в счётчиках.
"""
import os
import sys

sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

import goals_tester as G


def test_missing_on_site_goals_excluded(monkeypatch):
    monkeypatch.setattr(G, '_результаты_форм', lambda pid: {})
    monkeypatch.setattr(G, '_формные_цели', lambda pid: set())
    # Формы в прогоне были (есть проверенные формные url), но конкретной формы
    # subscribeform среди них нет → «Форма не в этом прогоне».
    monkeypatch.setattr(G, '_формные_url', lambda pid: {'https://x.ru/form'})
    # План: у цели «managerform» на этом сайте формы нет (СНГ-only).
    monkeypatch.setitem(
        G.ACTIONS, '__test_missing__',
        {'ожидаемые': [], 'нет_на_сайте': {'managerform': 'формы на РФ нет'}})

    каталог = {
        'проект': 'Тест', 'счётчик': '123', 'домен': 'x.ru',
        'цели': [
            {'тип': 'js', 'номер': 1, 'название': 'Отправка формы "Связаться с менеджером"',
             'условие': 'reachGoal managerform', 'идентификаторы': ['managerform']},
            {'тип': 'js', 'номер': 2, 'название': 'клик по кнопке',
             'условие': 'reachGoal call_ordering', 'идентификаторы': ['call_ordering']},
            {'тип': 'js', 'номер': 3, 'название': 'Отправленная форма подписки на рассылку',
             'условие': 'reachGoal subscribeform', 'идентификаторы': ['subscribeform']},
            {'тип': 'js', 'номер': 4, 'название': 'Рабочая цель',
             'условие': 'reachGoal a1', 'идентификаторы': ['a1']},
        ],
    }
    # ≥3 привязок → код считается «надёжным», поэтому call_ordering (нет в
    # привязках и в коде) уходит в «Нет в коде сайта», а a1 - в «Сработала (в коде)».
    прогон = {'fired': set(), 'страницы': [{'код': 200, 'url': 'https://x.ru/'}],
              'привязки': {'a1', 'a2', 'a3'}, 'код': '', 'идентификаторы': set()}

    res = G._классифицировать('__test_missing__', каталог, прогон)
    names = [s['название'] for s in res['строки']]

    assert 'Отправка формы "Связаться с менеджером"' not in names   # «Не найдена» убрана
    assert 'клик по кнопке' not in names                           # «Нет в коде сайта» убрана
    assert 'Отправленная форма подписки на рассылку' not in names  # «Форма не в этом прогоне» убрана
    assert 'Рабочая цель' in names                                 # обычная цель осталась
    assert all(s['статус'] not in ('Не найдена на сайте', 'Не найдено на сайте',
                                   'Нет в коде сайта', 'Форма не в этом прогоне')
               for s in res['строки'])
    # «Всего» согласован с фактически показанными строками.
    assert res['всего'] == len(res['строки']) == 1
    # Счётчики скрытых строк обнулены (managerform/subscribeform - manual,
    # call_ordering - no_code).
    assert res['счёт']['manual'] == 0
    assert res['счёт']['no_code'] == 0
    print('✓ цели «не найдено на сайте», «нет в коде сайта» и «форма не в этом '
          'прогоне» исключены из «Проверки целей»')


def test_проверяется_формами_excluded(monkeypatch):
    """Цель-подсказка «Проверяется формами» (формы в прогоне не гонялись) тоже
    не должна выводиться - никаких «запустите формы»."""
    monkeypatch.setattr(G, '_результаты_форм', lambda pid: {})
    monkeypatch.setattr(G, '_формные_цели', lambda pid: set())
    monkeypatch.setattr(G, '_формные_url', lambda pid: set())   # формы НЕ гонялись
    monkeypatch.setitem(G.ACTIONS, '__test_forms__', {'ожидаемые': [], 'нет_на_сайте': {}})

    каталог = {
        'проект': 'Тест', 'счётчик': '123', 'домен': 'x.ru',
        'цели': [
            {'тип': 'js', 'номер': 1, 'название': 'Отправленная форма подписки на рассылку',
             'условие': 'reachGoal subscribeform', 'идентификаторы': ['subscribeform']},
            {'тип': 'js', 'номер': 2, 'название': 'Рабочая цель',
             'условие': 'reachGoal a1', 'идентификаторы': ['a1']},
        ],
    }
    прогон = {'fired': set(), 'страницы': [{'код': 200, 'url': 'https://x.ru/'}],
              'привязки': {'a1', 'a2', 'a3'}, 'код': '', 'идентификаторы': set()}

    res = G._классифицировать('__test_forms__', каталог, прогон)
    names = [s['название'] for s in res['строки']]

    assert 'Отправленная форма подписки на рассылку' not in names   # «Проверяется формами» убрана
    assert 'Рабочая цель' in names
    assert all(s['статус'] != 'Проверяется формами' for s in res['строки'])
    assert res['всего'] == len(res['строки']) == 1
    assert res['счёт']['forms'] == 0
    print('✓ цель-подсказка «Проверяется формами» исключена из «Проверки целей»')


