"""Тест: цели со статусом «Не найдена/найдено на сайте» исключены из отчёта.

Если подтверждено, что кнопки/формы под цель на сайте просто нет (проверено
вручную через план «нет_на_сайте» или страница есть только на другом
поддомене), проверять там нечего - по просьбе такие строки в отчёт не выводим
и не учитываем в счётчиках.
"""
import os
import sys

sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

import goals_tester as G


def test_missing_on_site_goals_excluded(monkeypatch):
    monkeypatch.setattr(G, '_результаты_форм', lambda pid: {})
    monkeypatch.setattr(G, '_формные_цели', lambda pid: set())
    monkeypatch.setattr(G, '_формные_url', lambda pid: set())
    # План: у цели «managerform» на этом сайте формы нет (СНГ-only).
    monkeypatch.setitem(
        G.ACTIONS, '__test_missing__',
        {'ожидаемые': [], 'нет_на_сайте': {'managerform': 'формы на РФ нет'}})

    каталог = {
        'проект': 'Тест', 'счётчик': '123', 'домен': 'x.ru',
        'цели': [
            {'тип': 'js', 'номер': 1, 'название': 'Отправка формы "Связаться с менеджером"',
             'условие': 'reachGoal managerform', 'идентификаторы': ['managerform']},
            {'тип': 'js', 'номер': 2, 'название': 'Заказ звонка',
             'условие': 'reachGoal callback', 'идентификаторы': ['callback']},
        ],
    }
    прогон = {'fired': set(), 'страницы': [{'код': 200, 'url': 'https://x.ru/'}],
              'привязки': set(), 'код': '', 'идентификаторы': set()}

    res = G._классифицировать('__test_missing__', каталог, прогон)
    names = [s['название'] for s in res['строки']]

    assert 'Отправка формы "Связаться с менеджером"' not in names   # «Не найдена» убрана
    assert 'Заказ звонка' in names                                 # обычная цель осталась
    assert all(s['статус'] not in ('Не найдена на сайте', 'Не найдено на сайте')
               for s in res['строки'])
    # «Всего» согласован с фактически показанными строками.
    assert res['всего'] == len(res['строки']) == 1
    # Счётчик «вручную» больше не учитывает скрытую строку: остаётся только
    # «Заказ звонка» («Не проверено»), а managerform из счётчика вычтен.
    assert res['счёт']['manual'] == 1
    print('✓ цели «Не найдена/найдено на сайте» исключены из «Проверки целей»')


if __name__ == '__main__':
    import pytest
    raise SystemExit(pytest.main([__file__, '-v', '-s']))
