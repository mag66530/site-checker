"""Тест серверной валидации (пункт «нельзя отправить неверные данные через
DevTools»). Проверяются чистая функция-вердикт и защита от прогона на форме
заказа - без браузера."""
import sys
from pathlib import Path

import pytest

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT / "forms_tester"))

# test_all тянет bs4/playwright на уровне модуля - если их нет, тест пропускаем.
t = pytest.importorskip("test_all")


def test_принято_хотя_бы_раз_уязвима():
    статус, деталь = t.валидация_сервера_вердикт(
        {"empty": "отклонено", "bad_email": "принято", "too_long": "неприменимо"})
    assert статус == "УЯЗВИМА"
    assert "bad_email" in деталь


def test_все_применимые_отклонены_защищена():
    статус, деталь = t.валидация_сервера_вердикт(
        {"empty": "отклонено", "bad_email": "отклонено", "too_long": "неприменимо"})
    assert статус == "Защищена"
    assert деталь


def test_ничего_не_применимо_проверить():
    статус, деталь = t.валидация_сервера_вердикт(
        {"empty": "неприменимо", "bad_email": "неприменимо", "too_long": "неприменимо"})
    assert статус == "Проверить"


def test_пустой_словарь_проверить():
    статус, _ = t.валидация_сервера_вердикт({})
    assert статус == "Проверить"


def test_неоднозначный_результат_проверить():
    статус, _ = t.валидация_сервера_вердикт(
        {"empty": "не удалось определить", "bad_email": "неприменимо"})
    assert статус == "Проверить"


def test_принято_приоритетнее_отклонено():
    # Хотя бы одна принятая невалидная отправка - уязвимость, даже если
    # остальные виды честно отклонены.
    статус, _ = t.валидация_сервера_вердикт(
        {"empty": "отклонено", "bad_email": "принято", "too_long": "отклонено"})
    assert статус == "УЯЗВИМА"


def test_ответ_json_success_true_успешно():
    assert t._ответ_формы_вердикт('{"success": true}', 200) == "успешно"


def test_ответ_json_success_false_ошибка():
    assert t._ответ_формы_вердикт('{"success": false, "error": "bad"}', 200) == "ошибка"


def test_ответ_json_ok_поле():
    assert t._ответ_формы_вердикт('{"ok": true}', 200) == "успешно"
    assert t._ответ_формы_вердикт('{"ok": false}', 200) == "ошибка"


def test_ответ_csrf_невалидный_ошибка():
    assert t._ответ_формы_вердикт("CSRF token invalid", 400) == "ошибка"


def test_ответ_капча_блок_ошибка():
    assert t._ответ_формы_вердикт("Капча не пройдена, попробуйте снова", 200) == "ошибка"


def test_ответ_текст_спасибо_успешно():
    assert t._ответ_формы_вердикт("Спасибо, заявка принята", 200) == "успешно"


def test_ответ_голый_статус_200_успешно():
    # Нет ни JSON, ни узнаваемых фраз - слабый фолбэк на голый статус 200
    # (тот же принцип, что и в _interpret_response_status).
    assert t._ответ_формы_вердикт("", 200) == "успешно"


def test_строгий_голый_200_неизвестно():
    # Security-проба: голый 200 без маркеров - НЕ доказательство приёма
    # невалидных данных (форма могла отклонить их перерисовкой/текстом,
    # не попавшим в маркеры). Иначе рождается ложная «УЯЗВИМА» на каждой форме.
    assert t._ответ_формы_вердикт("", 200, строгий=True) == "неизвестно"
    assert t._ответ_формы_вердикт("<form>Заполните обязательные поля</form>", 200,
                                  строгий=True) == "неизвестно"


def test_строгий_явный_успех_всё_ещё_успешно():
    # Явный маркер приёма перекрывает строгий режим - настоящую уязвимую форму
    # (реально приняла пустое поле и ответила «спасибо»/success) не теряем.
    assert t._ответ_формы_вердикт('{"success": true}', 200, строгий=True) == "успешно"
    assert t._ответ_формы_вердикт("Спасибо, заявка принята", 200, строгий=True) == "успешно"


def test_строгий_явная_ошибка_всё_ещё_ошибка():
    # Узнаваемый отказ в строгом режиме по-прежнему «ошибка» (→ «отклонено» →
    # «Защищена»), а 4xx - тоже отказ.
    assert t._ответ_формы_вердикт('{"errors": ["fill"]}', 200, строгий=True) == "ошибка"
    assert t._ответ_формы_вердикт("Internal Server Error", 500, строгий=True) == "ошибка"


def test_ответ_статус_не_200_ошибка():
    assert t._ответ_формы_вердикт("Internal Server Error", 500) == "ошибка"


def test_ответ_без_статуса_неизвестно():
    # статус=None - ответ поймать не удалось (вызывающий код уйдёт в
    # визуальный фолбэк, а не спутает это с «сервер ответил ошибкой»).
    assert t._ответ_формы_вердикт("что угодно", None) == "неизвестно"


def test_форма_заказа_пропускается_без_браузера():
    # is_order=True - ранний выход ДО обращения к scope/page/sub (безопасно
    # вызвать с None - перестраховка на чекауте не должна трогать браузер).
    рез = t.проба_серверной_валидации(None, None, None, is_order=True)
    assert рез["попытки"] == {}
    assert "заказ" in рез["детали"]


def test_виды_нарушений_покрывают_обязательность_почту_длину():
    виды = dict(t._SRVVAL_ВИДЫ)
    assert set(виды) == {"empty", "bad_email", "too_long"}


def test_колонка_серверной_валидации_в_шапке_лога():
    assert "Серверная валидация" in t.LOG_HEADERS
    assert "серверная_валидация" in t.LOG_KEYS_ORDER
    assert len(t.LOG_HEADERS) == len(t.LOG_KEYS_ORDER)
    assert t.LOG_HEADERS.index("Серверная валидация") == \
        t.LOG_KEYS_ORDER.index("серверная_валидация")


