def where(название: str) -> str:
    """Подсказка «где находится форма» по её названию. Терпима к обрамлению:
    строки-названия в отчёте иногда приходят с пробелами/суффиксом города -
    сначала пробуем точное совпадение, потом по «голому» началу названия."""
    if not название:
        return ''
    имя = str(название).strip()
    if имя in FORM_WHERE:
        return FORM_WHERE[имя]
    # Иногда к названию приклеен суффикс/префикс (город, пометка) - ищем ключ,
    # который целиком содержится в названии (самый длинный - точнее).
    for ключ in sorted(FORM_WHERE, key=len, reverse=True):
        if ключ and ключ in имя:
            return FORM_WHERE[ключ]
    return ''
